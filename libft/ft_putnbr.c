#include "libft.h"

void	ft_putnbr(int nbr)
{
	char c;

	if (nbr == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nbr < 0)
		{
			write(1, "-", 1);
			nbr = (nbr * -1);
		}
		if (nbr > 9)
		{
			ft_putnbr(nbr / 10);
			nbr = nbr % 10;
		}
		c = nbr + '0';
		write(1, &c, 1);
	}
}
