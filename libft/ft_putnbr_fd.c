#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	char c;

	if (nbr == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (nbr < 0)
		{
			write(fd, "-", 1);
			nbr = (nbr * -1);
		}
		if (nbr > 9)
		{
			ft_putnbr_fd((nbr / 10), fd);
			nbr = nbr % 10;
		}
		c = nbr + '0';
		write(fd, &c, 1);
	}
}
